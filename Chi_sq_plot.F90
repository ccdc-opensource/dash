      SUBROUTINE plotting_Chi_sqd(ChiHandle)

      USE DRUID_HEADER
      USE WINTERACTER

      IMPLICIT NONE

      INTEGER, INTENT (IN   ) :: ChiHandle

      INCLUDE 'PARAMS.INC'
      INCLUDE 'poly_colours.inc'

!!<<<<<<< Chi_sq_plot.F90
!!      REAL, DIMENSION(MaxIter+1) :: Xarray

!!=======
!!>>>>>>> 1.28
      REAL                    chi_sqd
      INTEGER                                           it_count
      REAL                                                        y_max
      INTEGER                                                            MaxIterationSoFar
      REAL                    x_max, x_min, y_min
      LOGICAL                                      zoomed
      COMMON /CHISQDPLOTDATA/ chi_sqd(MaxIter, MaxRun), it_count, y_max, MaxIterationSoFar, &
                              x_max, x_min, y_min, zoomed
      LOGICAL         RESTART
      INTEGER                  Curr_SA_Run, NumOf_SA_Runs, MaxRuns, MaxMoves
      REAL                                                                    ChiMult
      COMMON /MULRUN/ RESTART, Curr_SA_Run, NumOf_SA_Runs, MaxRuns, MaxMoves, ChiMult

      REAL            bchmin, bpwval, bchpro, avchi1, avchi2, avchi3, avchi4
      INTEGER         nd1, nmpert, nd3, nd4, bmIHANDLE
      COMMON /sagdat/ bchmin, bpwval, bchpro, avchi1, avchi2, avchi3, avchi4, &
                      nd1, nmpert, nd3, nd4, bmIHANDLE

      INTEGER J, ISET
!!      REAL   x_max
      REAL   Xarray(MaxIter+1)

      CALL WindowSelect(ChiHandle)
      CALL WindowClear()

! MaxIterationSoFar is initialised to zero in BeginSa
! calculate array of x values for graph
      DO j = 1, MaxIterationSoFar
        Xarray(j) = FLOAT(j * nmpert)
      ENDDO

      IF (.NOT. Zoomed) THEN
        x_max = MaxIterationSoFar * FLOAT(nmpert)
        x_min = FLOAT(nmpert)
        y_min = 0.0
        y_max = chi_sqd(1, Curr_SA_Run)*1.25
      END IF
!  If zoomed, values for x_min etc taken from ChiSqdPlotData Common Block.
!
!  Start new presentation graphics plot
!
!  Set Clipping Rectangle
!
      CALL IPgClipRectangle('P')
!
!  Set style for each data set
!
      CALL IPgStyle(  1,  0,  0,  0,  KolNumobs)
!
!  Set marker number for data sets not using default marker
!
!
!  Set units for plot
!
      CALL IPgUnits(  x_min,      y_min,   &
                      x_max,      y_max)
!
!  Set presentation graphics area
!
      CALL IPgArea(0.20,0.300,0.900,0.900)
!
!  Draw main title
!
      CALL IGrCharSet('H')
      CALL IGrCharFont(3)
      CALL IGrCharSpacing('P')
      CALL IGrCharSize( 2.0, 1.5)
      CALL IGrColourN(KolNumMain)
      CALL IPgTitle('Profile Chi-squared vs. Number of Moves','C')
!
!  Label bottom X axis
!
      CALL IPgXLabelPos(  0.90)
      CALL IPgXLabel('Number of Moves','C')
!
!  Label left Y axis
!
      CALL IPgYLabelPos(  0.80)
      CALL IPgYLabelLeft('Profile Chi-squared','C9')
!
!  Draw axes
!
      CALL IGrColourN(KolNumMain)
      CALL IPgAxes()
!
!  Adjust tick position for X Axes
!
      CALL IPgXTickPos(     1.0000    ,    -1.0000    )
!
!  Scale for bottom X Axis
!
      CALL IPgXTickLength(  1.00)
      CALL IPgDecimalPlaces(      -1)
      CALL IPgXUserScale((/0.0/),0)
      CALL IPgXScaleAngle(  0.00,  -90.00)
      CALL IPgXScalePos(  0.1)
      CALL IPgXScale('NT')
!
!  Adjust tick position for Y Axes
!
      CALL IPgYTickPos(     1.0000    ,    -1.0000    )
!
!  Scale for left Y Axis
!
      CALL IPgYTickLength(  1.00)
      CALL IPgDecimalPlaces(      -1)
      CALL IPgYUserScale((/0.0/),0)
      CALL IPgYScaleAngle(  0.00,  0.00)
      CALL IPgYScalePos(  1.50)
      CALL IPgYScaleLeft('NT')
!
!  Draw graph.
!
      IF (NumOf_SA_Runs .GT. 0) THEN
! Plot Chi-sqd values to maximum number of moves for completed SA runs
        CALL IPgNewPlot(PgPolyLine,NumOf_SA_Runs,MaxIterationSoFar,0,1)
        DO j = 1, NumOf_SA_Runs
          CALL IPgStyle(  j,  0,  0,  0,  KolNumObs)
        ENDDO
        DO ISET = 1, NumOf_SA_Runs
          CALL IPgXYPairs(Xarray, Chi_sqd(1,ISET))
        ENDDO
      ENDIF
! Plot Chi-sqd values iteration by iteration
      CALL IPgNewPlot(PgPolyLine,1,it_count,0,1)
      CALL IPgStyle(  1,  0,  0,  0,  KolNumCal)
      CALL IPgXYPairs(Xarray, chi_sqd(1,Curr_SA_Run))

      END SUBROUTINE plotting_chi_sqd
